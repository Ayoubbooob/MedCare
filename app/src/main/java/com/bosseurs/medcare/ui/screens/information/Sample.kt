Le diabète est une maladie chronique grave qui se déclare lorsque le pancréas ne produit pas suffisamment d’insuline, ou lorsque l’organisme n’est pas capable d’utiliser efficacement l’insuline qu’il produit.

# Les types de Diabète
## Diabète de type 1
Le diabète de type 1 (autrefois appelé diabète insulino-dépendant ou juvénile) se caractérise par une production insuffisante d’insuline, laquelle doit être administrée quotidiennement.

La cause du diabète de type 1 n’est pas connue, et en l’état des connaissances actuelles, il n’est pas évitable.

Les symptômes sont les suivants : excrétion excessive d’urine (polyurie), sensation de soif (polydipsie), faim constante, perte de poids, altération de la vision et fatigue


## Diabète de type 2
Le diabète de type 2 (autrefois appelé diabète non insulino-dépendant ou diabète de la maturité) résulte d’une mauvaise utilisation de l’insuline par l’organisme.

Il est en grande partie le résultat d’une surcharge pondérale et du manque d’activité physique.

Ses symptômes peuvent similaires à ceux du diabète de type 1, mais sont souvent moins marqués. De ce fait, la maladie peut être diagnostiquée plusieurs années après son apparition, une fois les complications déjà présentes.


## Diabète gestationnel
Le diabète gestationnel se caractérise par la survenue, au cours de la grossesse, d’une élévation de la concentration de glucose dans le sang au-dessus des valeurs normales, mais à des valeurs inférieures à celles conduisant à poser le diagnostic de diabète.

Les femmes atteintes de diabète gestationnel ont un risque accru de complications pendant la grossesse et à l’accouchement. Leur risque ainsi que celui de leur enfant, d’avoir un diabète de type 2 à un stade ultérieur de leur vie augmente également.

